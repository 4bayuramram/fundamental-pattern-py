# Tipe data = jenis nilai dalam variabel

print("Tipe Data Python")


# STRING
# String = teks
nama = "Budi"
print("Nama:", nama)


# INTEGER
# Integer = angka tanpa koma
umur = 20
print("Umur:", umur)


# FLOAT
# Float = angka dengan koma
tinggi = 170.5
print("Tinggi:", tinggi)


# BOOLEAN
# Boolean = True atau False
lulus = True
print("Lulus:", lulus)


# LIST (mirip array di PHP)
# List = kumpulan data
buah = ["Apel", "Jeruk", "Mangga"]
print("Buah pertama:", buah[0])


